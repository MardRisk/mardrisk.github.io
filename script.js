// ── COUNTER — persists & grows convincingly across sessions ──
// Each page load: read saved base, add time-elapsed growth + small random jump.
// Ticks live with variable interval so it never looks scripted.
(function initCounter() {
  const K_N = 'mrk_n';   // count
  const K_T = 'mrk_t';   // last-save timestamp ms
  const K_D = 'mrk_d';   // date string YYYY-MM-DD

  const today = new Date().toISOString().slice(0, 10);
  const now   = Date.now();
  const hour  = new Date().getHours();
  const daytime = hour >= 8 && hour <= 23;

  const savedDay = localStorage.getItem(K_D);
  let base, lastT;

  if (savedDay === today) {
    // Same day — pick up where we left off
    base  = parseInt(localStorage.getItem(K_N)) || 130;
    lastT = parseInt(localStorage.getItem(K_T)) || now;
  } else {
    // New day — realistic morning baseline based on time of day
    base  = 75 + Math.floor(hour * 4.8) + Math.floor(Math.random() * 12);
    lastT = now;
    localStorage.setItem(K_D, today);
  }

  // Organic growth since last visit: ~1 user / 4 min day, 1 / 18 min night
  const elapsedMin = Math.max(0, (now - lastT) / 60000);
  const rate = daytime ? 0.25 : 0.055;
  const timeGrowth = Math.floor(elapsedMin * rate);

  // ✨ KEY FIX: random micro-jump per visit (1–4 users) — feels alive
  const visitJump = 1 + Math.floor(Math.random() * 3);

  window.userCount = Math.min(base + timeGrowth + visitJump, 412);

  localStorage.setItem(K_N, window.userCount);
  localStorage.setItem(K_T, now);
})();

function updateCounterUI(animate) {
  ['liveCount', 'proofCount', 'finalCountStat'].forEach(id => {
    const el = document.getElementById(id);
    if (!el) return;
    el.textContent = window.userCount;
    if (animate) {
      el.style.transition = 'transform .22s cubic-bezier(.34,1.8,.64,1), color .22s';
      el.style.transform  = 'scale(1.28)';
      el.style.color      = 'var(--green)';
      setTimeout(() => { el.style.transform = 'scale(1)'; el.style.color = ''; }, 380);
    }
  });
  document.querySelectorAll('#finalCount').forEach(el => el.textContent = window.userCount);
}

function tickCounter() {
  const hour    = new Date().getHours();
  const daytime = hour >= 8 && hour <= 23;
  // ~28% chance daytime, 5% night — feels human, not scripted
  if (Math.random() < (daytime ? 0.28 : 0.05)) {
    window.userCount++;
    localStorage.setItem('mrk_n', window.userCount);
    updateCounterUI(true);
  }
}

